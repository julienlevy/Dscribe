import UIKit

let onboardingIconWidth: CGFloat = 40

extension UIColor {
    class func dscribeDarkOrange() -> UIColor {
        return UIColor(red: 251.0/255, green: 183.0/255, blue: 59.0/255, alpha: 1.0)
    }
    class func dscribeLightOrange() -> UIColor {
        return UIColor(red: 254.0/255, green: 221.0/255, blue: 67.0/255, alpha: 1.0)
    }
    class func dscribeOrangeText() -> UIColor {
        return UIColor(red: 250.0/255, green: 193.0/255, blue: 62.0/255, alpha: 1.0)
    }
}